void main(List<String> args) {
  for (var deret = 0; deret < 10; deret += 2) {
    print("Iterasi dengan increment +2 hasilnya :" + deret.toString());
  }
  print("-----------------");

  for (var deret = 15; deret > 0; deret -= 3) {
    print('Iterasi dengan Decrement counter : ' + deret.toString());
  }
}
